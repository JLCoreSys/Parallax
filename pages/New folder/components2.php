                <strong>Sprites</strong> can be used to emphasize a point, display information in a different way, or to simply be added to the page to give it a little style.<br>
                Sprites are controlled in the same manor as key frame animation. You can have a single animation or chain multiple animations together. They can be tied to any of the frame states as the base time-line.<br>
                Have multiple animations in each frame state, or one in each, or just one animation in single state. The choice is yours.<br>
                <ul class="list">
                    <li>Timeline, keyframe animated style sprites</li>
                    <li>Chainable</li>
                    <li>Scale the width/height, change opacity, move, and rotate.</li>
                    <li>Scripting options for fluid displays. Dynamic settings for widths and heights.</li>
                    <li>Ability to add mathematical expressions into the settings for widths, heights, opacities, and positioning.</li>
                </ul>
