            <div class="span10 offset1 small-margin-top shadow whitebg black padding rounded5 parallax-scroll">
                <p><strong>Frames</strong> are essentially the pages of the site. These frames can be transitioned into view by fading them in or sliding them into view from various directions.<br>
                Each frame is completed customizable through HTML5 settings or Javascript settings with the ability to control the transition times, the time the frame remains in view and the type and direction of transition.</p>
                <p>
                    <ul class="list">
                        <li>Fame Options use the scrollbar like a time-line, 1000 scroll ticks is similar to 1000ms</li>
                        <li>5 States - Waiting, In-Transition, Paused, Out-Transition, Complete.</li>
                        <li>Frames are removed from the DOM when not needed for faster page response times.</li>
                        <li>Frame Types - Slide or Fade</li>
                        <li>Frame Slides - from the top,bottom,left or right</li>
                        <li>Progress indicator for paused pages (optional, enabled by default) - allow users to see how far they are into the frames paused state.</li>
                        <li>Customizable and scalable backgrounds - Just add your image with the parallax-frame-bg class and your image will always be properly dynamically fitted to the background. You will never see a black bar (if using default settings and css for backgrounds)</li>
                    </ul>
                </p>
