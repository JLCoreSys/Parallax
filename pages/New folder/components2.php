                <p><strong>Parallax</strong> is completely customizable through HTML or Javascript settings.</p>
                <p>There are options for a loader, which is a true loader that will preload all your content and display a loading screen while waiting for the page to setup. Parallax is fast, and the only delays in loading are the components in the HTML.</p>
                <p>
                    <ul class="list">
                        <li>Built in Loader - ability to enable/disable (enabled by default)</li>
                        <li>Built in Navigation - ability to enable/disable (enabled by default)</li>
                        <li>Custom CSS - All components have customizable CSS (even at load time)</li>
                        <li>Tested on IE, Chrome, Firefox, Safari, and Opera</li>
                        <li>Works on Mobile devices</li>
                    </ul>
                </p>
            </div>
